package programs;

public class Comparisonandlogicalop {
public static void main(String[]args) {
int x=35;
int y=35;
System.out.println("COMPARISON OPERATORS");
System.out.println(x<y);
System.out.println(x>y);
System.out.println(x==y);
System.out.println(x>=y);
System.out.println(x<=y);
System.out.println(x!=y);
System.out.println("LOGICAL OPERATORS");
System.out.println((x<y)&&(x==y));
System.out.println((x<y)||(x==y));
System.out.println(!(x<y)&&(x==y));
}

}
